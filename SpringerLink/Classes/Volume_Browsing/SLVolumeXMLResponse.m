#import "SLVolumeXMLResponse.h"


@implementation SLVolumeXMLResponse

@synthesize records               = _records;
@synthesize journalName           = _journalName;
@synthesize JournalPrintISSN      = _JournalPrintISSN;
@synthesize JournalElectronicISSN = _JournalElectronicISSN;
@synthesize JournalID             = _JournalID;
@synthesize Editor_in_Chief       = _Editor_in_Chief;
@synthesize publisherName         =_publisherName;


- (id)init {
    self = [super init];
    
    if (self != nil) {
        
        _records               = [NSMutableArray new];
        _journalName           = [NSString new];
        _JournalPrintISSN      = [NSString new];
        _JournalElectronicISSN = [NSString new];
        _JournalID             = [NSString new];
        _Editor_in_Chief       = [NSString new];
        _publisherName       = [NSString new];
    }
    return self;
}

@end


